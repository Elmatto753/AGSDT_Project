#ifndef RAY_H_
#define RAY_H_

#include <ngl/Vec3.h>

typedef struct Ray
{
  //----------------------------------------------------------------------------------------------------------------------
    /// @brief Start point for the ray
  //----------------------------------------------------------------------------------------------------------------------
  ngl::Vec3 m_StartPoint = ngl::Vec3(0.0f, 0.0f, 0.0f);
  //----------------------------------------------------------------------------------------------------------------------
    /// @brief End point for the ray
  //----------------------------------------------------------------------------------------------------------------------
  ngl::Vec3 m_EndPoint = ngl::Vec3(0.0f, 0.0f, 0.0f);
  //----------------------------------------------------------------------------------------------------------------------
    /// @brief Direction vector for the ray
  //----------------------------------------------------------------------------------------------------------------------
  ngl::Vec3 m_Direction = ngl::Vec3(0.0f, 1.0f, 0.0f);

}Ray;


#endif
